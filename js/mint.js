async function getAccounts() {
    await window.ethereum.enable();
    const accounts = await ethereum.request({ method: 'eth_requestAccounts' });
    return accounts;
}

async function mint(quantity) {
    if(quantity <= 0 || quantity > 50) {
        throw new Error("min quantity 1, max quantity 50");
    }

    await window.ethereum.enable();
    const accounts = await getAccounts();
    if(accounts.length <= 0) {
        throw new Error("connect to metamask");
    }

    const mintAmt = (.1* quantity).toString();
    const amount = web3.utils.toWei(mintAmt, 'ether');
    const value = web3.utils.toHex(amount);

    const tx = {
        'from': accounts[0],
        'to': CITADEL_NFT,
        'value': value,
        'data': nftContract.methods.mintCitadel(quantity).encodeABI()
    };

      // txHash is a hex string
      // As with any RPC call, it may throw an error

    const txHash = await window.ethereum.request({
        method: 'eth_sendTransaction',
        params: [tx],
    });

    return txHash;
  }